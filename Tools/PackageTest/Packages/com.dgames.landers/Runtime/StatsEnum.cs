namespace Landers
{
    /// <summary>
    /// Defines the various statistics (stats) used in the context of a lander
    /// </summary>
    public enum StatsEnum
    {
        /// <summary>
        /// Represents the "Pv" stat, which could refer to a lander's health or another Pv-related attribute.
        /// </summary>
        Pv,

        /// <summary>
        /// Represents the "Attack" stat, which defines the offensive capability of a lander.
        /// </summary>
        Attack,

        /// <summary>
        /// Represents the "Defense" stat, which defines the defensive capabilities of a lander.
        /// </summary>
        Defense,

        /// <summary>
        /// Represents the "AttackSpecial" stat, which defines special offensive abilities of a lander.
        /// </summary>
        AttackSpecial,

        /// <summary>
        /// Represents the "DefenseSpecial" stat, which defines special defensive abilities of a lander.
        /// </summary>
        DefenseSpecial,

        /// <summary>
        /// Represents the "Speed" stat, which defines how fast a lander can act or react.
        /// </summary>
        Speed
    }
}
